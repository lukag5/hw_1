mood = str(input("What mood are you in? "))

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times!")
elif mood == "sad":
    print("Take a walk!")
elif mood == "excited":
    print("Good to hear!")
elif mood == "relaxed":
    print("Keep on going!")
else:
    print("I don't recognize this mood.")